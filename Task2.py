sn = int(input("Введите номер места (от 1 до 54): "))

if 1 <= sn <= 54:
    if sn % 2 == 0:
        h = "верхнее"
    else:
        h = "нижнее"

    if 1 <= sn <= 36:
        type = "купе"
    else:
        type = "боковое"

    print(f"Место {sn}: {h} {type}")
else:
    print("Некорректный номер места. Введите число от 1 до 54.")